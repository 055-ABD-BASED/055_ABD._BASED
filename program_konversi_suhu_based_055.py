# Konversi_Suhu_Celsius_Ke_Fahrenhait

celsius = int (input("masukkan nilai suhu c yang ingin anda konversi ke suhu f = "))
conv_cel = 9*celsius/5+32

print (f'hasil konversi {celsius} C menjadi {conv_cel}F')



# konversi_Suhu_Fahrenhit_Ke_Celsius

kelvin = int (input("masukkan nilai suhu K yang ingin anda konversi ke suhu c ="))
konv_k = kelvin - 273.15

print (f'hasil konversi {kelvin} K menjadi {konv_k} C')